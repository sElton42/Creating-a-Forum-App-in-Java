    public void adicionarResp(Resposta res){
        Topico t=ForumMTP.listaTopicos.get(0);
        
        System.out.println("");
        System.out.println(tituloTopico.getText() + "\n");
        
        for(int i = 0; i<ForumMTP.listaTopicos.size(); i++){
            if(tituloTopico.getText().equals(ForumMTP.listaTopicos.get(i).getTitulo())){
                t = ForumMTP.listaTopicos.get(i);
                t.addResp(res);
            }
        }
        
        System.out.println(t + "\n");
        
        for(int i = 0; i<ForumMTP.listaTopicos.size(); i++){
            if(ForumMTP.listaTopicos.get(i).getTitulo().equals(t.getTitulo())){
                Gerenciadores.RemovedorTopico(ForumMTP.listaTopicos.get(i));
            }
        }
        
        ForumMTP.listaTopicos.add(t);
        
        for(int i = 0; i<ForumMTP.listaTopicos.size();i++){
            System.out.println(ForumMTP.listaTopicos.get(i));
        }
        
        Gerenciadores.escreverArquivoTopicosFalse(t);
        
        for(int i = 0; i<ForumMTP.listaTopicos.size(); i++){
            if(!ForumMTP.listaTopicos.get(i).getTitulo().equals(t.getTitulo())){
                Gerenciadores.escreverArquivoTopicos(ForumMTP.listaTopicos.get(i));
            }
        }
        
    }
    
